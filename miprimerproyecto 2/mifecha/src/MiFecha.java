public class MiFecha {
    private String dia;
    private String mes;
    private Int anio;

    public String getDia() {
        return dia;
    }

    public void setDia(String dia) {
        this.dia = dia;
    }

    public String getMes() {
        return mes;
    }

    public void setMes(String mes) {
        this.mes = mes;
    }

    public Int getAnio() {
        return anio;
    }

    public void setAnio(Int anio) {
        this.anio = anio;
    }

}
